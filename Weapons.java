class Weapons{


	private String name;
	private int manufacturedIn;
	private int range;
	private int shellVelocity;
	
	
	
	
			
				public String getname(){
			return name;
			}
			
			public void  setname(String name){
				this.name = name;
			}

			public int getmanufacturedIn(){
			return manufacturedIn;
			}

			public void setmanufacturedIn(int manufacturedIn){
			this.manufacturedIn = manufacturedIn;
			
			}

			public int getrange(){
			return range;
			}
			
			public void setrange(int range){
			this.range = range;
			}

			public int getshellVelocity(){
			return shellVelocity;
			}

			public void setshellVelocity(int shellVelocity){
			this.shellVelocity = shellVelocity; 	
			}




}