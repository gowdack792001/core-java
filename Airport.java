class Airport{



	private String name;
	private String area;
	private String openedIn;
	
	private String otherFacility;
	
	
		public String getname(){
			return name;
			}
			
			public void  setname(String name){
				this.name = name;
			}

			public String getarea(){
			return area;
			}

			public void setarea(String area){
			this.area = area;
			
			}

			public String getopenedIn(){
			return openedIn;
			}
			
			public void setopenedIn(String openedIn){
			this.openedIn = openedIn;
			}

			public String getotherFacility(){
			return otherFacility;
			}

			public void setotherFacility(String otherFacility){
			this.otherFacility = otherFacility; 	
			}



}