class BigBasket{


 private String companyName;
private String type;

private String foundedIn;
private String headquarterIn;


		public String getcompanyName(){
			return companyName;
			}
			
			public void  setcompanyName(String companyName){
				this.companyName = companyName;
			}

			public String gettype(){
			return type;
			}

			public void settype(String type){
			this.type = type;
			
			}

			public String getfoundedIn(){
			return foundedIn;
			}
			
			public void setfoundedIn(String foundedIn){
			this.foundedIn = foundedIn;
			}

			public String getheadquarterIn(){
			return headquarterIn;
			}

			public void setheadquarterIn(String headquarterIn){
			this.headquarterIn = headquarterIn; 	
			}








}